package com.example.androidjava.Model;

public class mOrderItem {
    private String id;
    private String product_id;
    private String product_name;
    private String img_product;
    private String product_qut;
    private String shop_id;
    private String product_price;
    private String total_item_amount;
    private String order_status;
    private String order_id;
    private String order_date;

    public mOrderItem() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getProduct_id() {
        return product_id;
    }

    public void setProduct_id(String product_id) {
        this.product_id = product_id;
    }

    public String getProduct_name() {
        return product_name;
    }

    public void setProduct_name(String product_name) {
        this.product_name = product_name;
    }

    public String getImg_product() {
        return img_product;
    }

    public void setImg_product(String img_product) {
        this.img_product = img_product;
    }

    public String getProduct_qut() {
        return product_qut;
    }

    public void setProduct_qut(String product_qut) {
        this.product_qut = product_qut;
    }

    public String getShop_id() {
        return shop_id;
    }

    public void setShop_id(String shop_id) {
        this.shop_id = shop_id;
    }

    public String getProduct_price() {
        return product_price;
    }

    public void setProduct_price(String product_price) {
        this.product_price = product_price;
    }

    public String getTotal_item_amount() {
        return total_item_amount;
    }

    public void setTotal_item_amount(String total_item_amount) {
        this.total_item_amount = total_item_amount;
    }

    public String getOrder_status() {
        return order_status;
    }

    public void setOrder_status(String order_status) {
        this.order_status = order_status;
    }

    public String getOrder_id() {
        return order_id;
    }

    public void setOrder_id(String order_id) {
        this.order_id = order_id;
    }

    public String getOrder_date() {
        return order_date;
    }

    public void setOrder_date(String order_date) {
        this.order_date = order_date;
    }
}
